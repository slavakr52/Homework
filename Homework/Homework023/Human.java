public class Human {
    
    private String name;
    private Integer age;

    public Human(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

    public String getInfo() {
        return "Name: " + name + ", Age=" + age;
    }

    public String getParentsInfo() {
        return "Parent: " + name + ", Age=" + age;
    }

    public void setInfo(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

    
    



    



    



    
    

    


    
}
